#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	size_t	len;

	len = ft_strlen(src);
	dst = malloc(sizeof(char) * (len + 1));
	ft_memcpy(dst, src, len + 1);
	return (dst);
}
